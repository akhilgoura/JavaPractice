public class Literals {
    public static void main(String[] args) {
        int age = 23; //Here 23 is a literal
        long phoneNumber = 7018134555L; // With long L is used to signify it's a long
        char firstLetterOfName = 'A'; // Here A is a literal
        float floatingNumber = 5.55f; //We need f in the last of float variable
        double accountBalance = 2122.55d; // Here 2122.55d is a literal ,and it's a value that can be stored in a variable
        Boolean isTrue = true; // true is a literal here

        System.out.println(age);
        System.out.println(phoneNumber);
        System.out.println(firstLetterOfName);
        System.out.println(floatingNumber);
        System.out.println(accountBalance);
        System.out.println(isTrue);
    }
}
